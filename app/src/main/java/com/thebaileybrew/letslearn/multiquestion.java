package com.thebaileybrew.letslearn;

public class multiquestion {

    String firstDigit;
    String secondDigit;
    String correctAnswer;
    String mathFunction;
    String incorrectOne;
    String incorrectTwo;
    String incorrectThree;

    public multiquestion(String firstDigit, String secondDigit, String correctAnswer, String mathFunction, String incorrectOne, String incorrectTwo, String incorrectThree) {
        this.firstDigit = firstDigit;
        this.secondDigit = secondDigit;
        this.correctAnswer = correctAnswer;
        this.mathFunction = mathFunction;
        this.incorrectOne = incorrectOne;
        this.incorrectTwo = incorrectTwo;
        this.incorrectThree = incorrectThree;
    }
}
